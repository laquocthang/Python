s="   day la chuoi bat ky! Ahihi    "

print(s)                    #Xuât chuỗi ban đầu

print(s.capitalize())       #Ghi hoa chữ cái đầu tiên

print(s.title())            #Ghi hoa từng từ

print(s.center(50),"!!!")   #Tạo một trường có độ rộng 50 và căn giữa chuỗi đó

print(s.lower())            #Ghi chữ thường

print(s.upper())            #Ghi chữ hoa

print(s.lstrip())           #Xóa khoảng trắng ngay đầu

print(s.replace("Ahihi","Hihi"))    #Thay chữ Ahihi thành Hihi

print(s.rfind("bat"))       #Trả về vị trí từ phải sang trái

print(s.rjust(50))          #Căn đều sang lề phải

print(s.rstrip())           #Xóa khoảng trắng ở cuối chuỗi